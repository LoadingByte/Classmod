package com.quartercode.classmod.test.def.extra.prop;

import java.util.List;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;
import com.quartercode.classmod.extra.storage.Storage;

@XmlRootElement
class DummyListStorage<T> extends Storage<List<T>> {

    @XmlElement (name = "list")
    private List<T> object;

    @Override
    public List<T> get() {

        return object;
    }

    @Override
    public void set(List<T> object) {

        this.object = object;
    }

    @Override
    public Storage<List<T>> reproduce() {

        return new DummyListStorage<>();
    }

    @Override
    public int hashCode() {

        return HashCodeBuilder.reflectionHashCode(this);
    }

    @Override
    public boolean equals(Object obj) {

        return EqualsBuilder.reflectionEquals(this, obj);
    }

    @Override
    public String toString() {

        return ToStringBuilder.reflectionToString(this);
    }

}
